"""
    Los condicionales son estructuras que controlan el flujo de ejecución
    de nuestro programa dada una condición, es decir, si la condición resulta cierta
    (verdadera) se ejecutará un bloque de código y en caso contrario (falso), se 
    ejecutará otro bloque de código. 

    En Python disponemos de tres estructuras condicionales:
        
        * Condicionales simples:
            if condición:
                Código

        * Condicionales if ... else:
            if condición:
                Código #1
            else:
                Código #2 

        * Condicionales if .. elif ... else:
            if condición #1:
                Código #1
            elif condición #2:
                Código #2
            elif condición #3:
                Código #3
            else:
                Código #4
"""